#include <stdio.h>
int main(){
    int x = 45;
    float y = 234.52;
    char dv = 'a';

    // printing data types: 
    // 1. int --> %d
    // 2. float --> %f
    // 3. char --> %c
    printf("Hello World\n");
    printf("The value of x is %d\n", x);
    printf("The value of y is %f\n", y);
    printf("the value of \t dv is %c\n", dv);
    return 0;
}

// escape sequence :
// 1. \n --> new line
// 2. \t --> tab

// integral value : 
// 1. short 23
// 2. int 38472
// 3. long 329472732907

// decimal value :
// 1. float 234.45
// 2. double 2389664.67

// character value :
